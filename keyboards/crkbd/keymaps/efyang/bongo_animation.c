uint32_t anim_timer = 0;
uint32_t anim_sleep = 0;
uint8_t current_idle_frame = 0;
uint8_t current_tap_frame = 0;

#define max(x,y) (((x) >= (y)) ? (x) : (y))

// Images credit j-inc(/James Incandenza) and pixelbenny. Credit to obosob for initial animation approach.
void render_anim(void) {
    //assumes 1 frame prep stage
    void animation_phase(void) {
        if(get_current_wpm() <=IDLE_SPEED){
            current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
            oled_write_raw_P(idle_frames[current_idle_frame], ANIM_SIZE);
#ifdef BONGO_PARTY_HAT
#endif
        } else if(get_current_wpm() >IDLE_SPEED && get_current_wpm() <TAP_SPEED){
            oled_write_raw_P(ready, ANIM_SIZE);  // remove if IDLE_FRAMES >1
#ifdef BONGO_PARTY_HAT
#endif
        } else if(get_current_wpm() >=TAP_SPEED){
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            oled_write_raw_P(tap_frames[current_tap_frame], ANIM_SIZE);
#ifdef BONGO_PARTY_HAT
#endif
        }
    }

    if(get_current_wpm() != 0) {
        oled_on(); // not essential but turns on animation OLED with any alpha keypress

        if(timer_elapsed32(anim_timer) >
                max(ANIM_FRAME_DURATION - (uint32_t)get_current_wpm()/WPM_FRAME_FACTOR, MIN_ANIM_FRAME_DURATION)) {
            anim_timer = timer_read32();
            animation_phase();
        }

        anim_sleep = timer_read32();
    } else {
        if(timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
            oled_off();
        } else {
            if(timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
                anim_timer = timer_read32();
                animation_phase();
            }
        }
    }
}
